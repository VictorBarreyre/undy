import PushNotificationIOS from '@react-native-community/push-notification-ios';
import { Platform, Alert, AppState } from 'react-native';
import AsyncStorage from '@react-native-async-storage/async-storage';
import Constants from 'expo-constants';

// Fonction pour d√©tecter si on est sur simulateur
const isSimulator = () => {
  return !Constants.isDevice;
};

class NotificationService {
  constructor() {
    this.isConfigured = false;
    this.notificationListeners = [];
    this.removeListeners = [];
    this.processedNotifications = new Set(); // AJOUT: D√©duplication des notifications
  }

  // M√©thode pour nettoyer les listeners en double
  cleanupDuplicateListeners() {
    console.log('[NotificationService] üßπ Nettoyage des listeners dupliqu√©s');
    console.log('[NotificationService] üìä Listeners avant nettoyage:', this.notificationListeners.length);
    
    // Garder seulement les listeners uniques (par r√©f√©rence de fonction)
    const uniqueListeners = [];
    const seenListeners = new Set();
    
    this.notificationListeners.forEach(listener => {
      if (!seenListeners.has(listener)) {
        seenListeners.add(listener);
        uniqueListeners.push(listener);
      }
    });
    
    this.notificationListeners = uniqueListeners;
    console.log('[NotificationService] üìä Listeners apr√®s nettoyage:', this.notificationListeners.length);
  }

  // Initialiser le service
  async initialize() {
    if (this.isConfigured) return true;

    try {
      if (Platform.OS === 'ios') {
        PushNotificationIOS.setApplicationIconBadgeNumber(0);

        // IMPORTANT: V√©rifier que les handlers sont d√©finis avant de les ajouter
        if (typeof this.onRemoteNotification === 'function') {
          this.removeListeners.push(
            PushNotificationIOS.addEventListener('notification', this.onRemoteNotification)
          );
        }

        if (typeof this.onLocalNotification === 'function') {
          this.removeListeners.push(
            PushNotificationIOS.addEventListener('localNotification', this.onLocalNotification)
          );
        }

        if (typeof this.onRegistered === 'function') {
          this.removeListeners.push(
            PushNotificationIOS.addEventListener('register', this.onRegistered)
          );
        }

        if (typeof this.onRegistrationError === 'function') {
          this.removeListeners.push(
            PushNotificationIOS.addEventListener('registrationError', this.onRegistrationError)
          );
        }
      }

      this.isConfigured = true;
      return true;
    } catch (error) {
      console.error('[NotificationService] Erreur initialisation:', error);
      return false;
    }
  }

  // Demander les permissions et obtenir le token
  async requestPermissions() {
    try {
      if (isSimulator()) {
        console.log('[NotificationService] Simulateur d√©tect√©');
        return { granted: true, token: 'SIMULATOR_MOCK_TOKEN' };
      }

      if (Platform.OS === 'ios') {
        // Demander les permissions pour iOS
        const permissions = await PushNotificationIOS.requestPermissions({
          alert: true,
          badge: true,
          sound: true,
        });

        console.log('[NotificationService] Permissions iOS:', permissions);

        if (permissions.alert || permissions.badge || permissions.sound) {
          // Attendre que le token soit enregistr√©
          return new Promise((resolve) => {
            const timeout = setTimeout(() => {
              resolve({ granted: true, token: null });
            }, 5000);

            // √âcouter l'√©v√©nement d'enregistrement
            const removeListener = PushNotificationIOS.addEventListener('register', (token) => {
              clearTimeout(timeout);
              removeListener();
              console.log('[NotificationService] Token APNs re√ßu:', token);
              resolve({ granted: true, token });
            });

            // √âcouter les erreurs
            const removeErrorListener = PushNotificationIOS.addEventListener('registrationError', (error) => {
              clearTimeout(timeout);
              removeListener();
              removeErrorListener();
              console.error('[NotificationService] Erreur enregistrement:', error);
              resolve({ granted: true, token: null });
            });
          });
        }

        return { granted: false, token: null };
      } else {
        console.log('[NotificationService] Android non support√© avec cette librairie');
        return { granted: false, token: null };
      }
    } catch (error) {
      console.error('[NotificationService] Erreur permissions:', error);
      return { granted: false, token: null };
    }
  }

  // Callback quand le token est re√ßu
  onRegistered = (deviceToken) => {
    console.log('[NotificationService] Token enregistr√©:', deviceToken);
    AsyncStorage.setItem('apnsToken', deviceToken).catch(console.error);
  }

  // Callback pour les erreurs d'enregistrement
  onRegistrationError = (error) => {
    console.error('[NotificationService] Erreur enregistrement token:', error);
  }

  // Callback pour les notifications re√ßues - VERSION S√âCURIS√âE
  onRemoteNotification = (notification) => {
    console.log('[NotificationService] Notification re√ßue:', notification);
    
    // √âviter les appels multiples
    if (notification._remoteNotificationCompleteCallbackCalled) {
      console.log('[NotificationService] ‚ö†Ô∏è Notification d√©j√† trait√©e, skip');
      return;
    }
    
    const isUserInteraction = notification.userInteraction || 
                            (AppState.currentState !== 'active');
    
    console.log('[NotificationService] User interaction:', isUserInteraction);
    console.log('[NotificationService] App state:', AppState.currentState);
    
    // Traiter la notification
    if (isUserInteraction) {
      console.log('[NotificationService] üëÜ Traitement comme interaction utilisateur');
      this.handleNotificationOpen(notification);
    } else {
      console.log('[NotificationService] üì± App active, affichage en foreground');
      this.handleForegroundNotification(notification);
    }
    
    console.log('[NotificationService] ‚úÖ Traitement termin√©');
    
    // SOLUTION S√âCURIS√âE: Ne pas appeler finish() pour √©viter les erreurs
    // Le syst√®me iOS g√®re automatiquement la compl√©tion dans la plupart des cas
    notification._remoteNotificationCompleteCallbackCalled = true;
    
    console.log('[NotificationService] üîí Notification marqu√©e comme trait√©e (sans finish() pour √©viter les erreurs)');
  }

  // G√©rer l'ouverture d'une notification avec d√©duplication
  handleNotificationOpen = (notification) => {
    console.log('[NotificationService] üéØ handleNotificationOpen appel√©');

    let data = null;

    // Extraction des donn√©es
    if (notification && notification._data) {
      data = notification._data;
    } else if (notification && notification.data) {
      data = notification.data;
    } else if (notification && typeof notification.getData === 'function') {
      try {
        data = notification.getData();
      } catch (error) {
        console.error('[NotificationService] Erreur getData():', error);
        data = notification._data || notification.data || {};
      }
    } else if (notification && notification.userInfo) {
      data = notification.userInfo;
    } else {
      data = notification || {};
    }

    // AJOUT: D√©duplication par notification ID
    const notificationId = data.notificationId || data.id || Date.now().toString();
    
    if (this.processedNotifications.has(notificationId)) {
      console.log('[NotificationService] ‚ö†Ô∏è Notification d√©j√† trait√©e:', notificationId);
      return;
    }
    
    this.processedNotifications.add(notificationId);
    
    // Nettoyer apr√®s 30 secondes
    setTimeout(() => {
      this.processedNotifications.delete(notificationId);
    }, 30000);
    
    console.log('[NotificationService] üìä Donn√©es extraites:', JSON.stringify(data, null, 2));
    console.log('[NotificationService] üë• Nombre de listeners:', this.notificationListeners.length);

    // Appeler les listeners UNE SEULE FOIS
    this.notificationListeners.forEach((listener, index) => {
      console.log(`[NotificationService] üì£ Appel du listener ${index + 1}/${this.notificationListeners.length}`);
      try {
        listener(data);
        console.log(`[NotificationService] ‚úÖ Listener ${index + 1} ex√©cut√© avec succ√®s`);
      } catch (error) {
        console.error(`[NotificationService] ‚ùå Erreur listener ${index + 1}:`, error);
      }
    });

    // Log sp√©cifique pour le type de notification
    if (data?.type === 'new_message' && data?.conversationId) {
      console.log('[NotificationService] üí¨ Notification de message d√©tect√©e');
      console.log('[NotificationService] üÜî ConversationId:', data.conversationId);
      console.log('[NotificationService] üë§ SenderId:', data.senderId);
      console.log('[NotificationService] üìù SenderName:', data.senderName);
      console.log('[NotificationService] üì± MessageType:', data.messageType);
      
    }
  }

  // G√©rer les notifications en foreground
  handleForegroundNotification = (notification) => {
    let data = {};
    let alert = {};

    // Extraire les donn√©es et l'alerte
    if (notification._data) {
      data = notification._data;
    }
    if (notification._alert) {
      alert = notification._alert;
    }

    if (alert.title && alert.body) {
      // Afficher une alerte
      Alert.alert(
        alert.title,
        alert.body,
        [
          { text: 'Ignorer', style: 'cancel' },
          {
            text: 'Voir',
            onPress: () => this.handleNotificationOpen(notification)
          }
        ]
      );
    }
  }

  // Ajouter un listener pour les notifications avec d√©duplication am√©lior√©e
  addNotificationListener(callback) {
    console.log('[NotificationService] üéØ Tentative d\'ajout de listener');
    console.log('[NotificationService] üìä √âtat actuel:', {
      listenersCount: this.notificationListeners.length,
      callbackType: typeof callback
    });
    
    // V√©rification du callback
    if (typeof callback !== 'function') {
      console.error('[NotificationService] ‚ùå Le callback n\'est pas une fonction');
      return () => {}; // Retourner une fonction vide pour √©viter les erreurs
    }
    
    // √âviter les doublons en v√©rifiant la r√©f√©rence ET le contenu
    const callbackString = callback.toString();
    const isDuplicate = this.notificationListeners.some(existingCallback => {
      return existingCallback === callback || existingCallback.toString() === callbackString;
    });
    
    if (!isDuplicate) {
      this.notificationListeners.push(callback);
      console.log('[NotificationService] ‚ûï Listener ajout√©, total:', this.notificationListeners.length);
    } else {
      console.log('[NotificationService] ‚ö†Ô∏è Listener d√©j√† existant, ignor√©');
    }
    
    // Nettoyer p√©riodiquement si trop de listeners
    if (this.notificationListeners.length > 3) {
      console.log('[NotificationService] üßπ Nettoyage automatique d√©clench√©');
      this.cleanupDuplicateListeners();
    }
    
    // Retourner une fonction pour retirer le listener
    return () => {
      console.log('[NotificationService] üóëÔ∏è Suppression du listener demand√©e');
      const index = this.notificationListeners.indexOf(callback);
      if (index > -1) {
        this.notificationListeners.splice(index, 1);
        console.log('[NotificationService] ‚ûñ Listener retir√©, total:', this.notificationListeners.length);
      } else {
        console.log('[NotificationService] ‚ö†Ô∏è Listener non trouv√© lors de la suppression');
      }
    };
  }

  // Obtenir le token stock√©
  async getToken() {
    try {
      if (isSimulator()) {
        return 'SIMULATOR_MOCK_TOKEN';
      }

      const token = await AsyncStorage.getItem('apnsToken');
      return token;
    } catch (error) {
      console.error('[NotificationService] Erreur r√©cup√©ration token:', error);
      return null;
    }
  }

  // Envoyer une notification locale
  async sendLocalNotification(title, body, data = {}) {
    try {
      if (Platform.OS === 'ios') {
        PushNotificationIOS.addNotificationRequest({
          id: String(Date.now()),
          title: title,
          body: body,
          userInfo: data,
          sound: 'default',
        });

        console.log('[NotificationService] Notification locale programm√©e');
        return true;
      }
      return false;
    } catch (error) {
      console.error('[NotificationService] Erreur notification locale:', error);
      return false;
    }
  }

  // Obtenir le nombre de badges
  async getBadgeCount() {
    if (Platform.OS === 'ios') {
      return new Promise((resolve) => {
        PushNotificationIOS.getApplicationIconBadgeNumber((num) => {
          resolve(num);
        });
      });
    }
    return 0;
  }

  // D√©finir le nombre de badges
  async setBadgeCount(count) {
    if (Platform.OS === 'ios') {
      PushNotificationIOS.setApplicationIconBadgeNumber(count);
    }
  }

  // Effacer toutes les notifications
  async clearAllNotifications() {
    if (Platform.OS === 'ios') {
      PushNotificationIOS.removeAllDeliveredNotifications();
      await this.setBadgeCount(0);
    }
  }

  // Ajouter une m√©thode pour la compatibilit√©
  async checkPermissions() {
    if (Platform.OS === 'ios') {
      return new Promise((resolve) => {
        PushNotificationIOS.checkPermissions((permissions) => {
          resolve(permissions.alert || permissions.badge || permissions.sound);
        });
      });
    }
    return false;
  }

  // M√©thode pour envoyer une notification de test
  async sendTestNotification() {
    try {
      await this.sendLocalNotification(
        'Test de notification',
        'Les notifications fonctionnent correctement !',
        { type: 'test' }
      );
      return { success: true };
    } catch (error) {
      console.error('[NotificationService] Erreur test notification:', error);
      return { success: false, error: error.message };
    }
  }

  // Nettoyer les listeners
  cleanup() {
    if (Platform.OS === 'ios') {
      // Retirer tous les listeners enregistr√©s
      this.removeListeners.forEach(remove => {
        if (typeof remove === 'function') {
          remove();
        }
      });
      this.removeListeners = [];
    }
    this.notificationListeners = [];
    this.processedNotifications.clear(); // Nettoyer les notifications trait√©es
  }

  // M√©thode pour annuler toutes les notifications
  async cancelAllNotifications() {
    if (Platform.OS === 'ios') {
      PushNotificationIOS.removeAllDeliveredNotifications();
      await this.setBadgeCount(0);
    }
  }

  // M√©thode pour forcer le nettoyage des listeners (debug)
  forceCleanupListeners() {
    console.log('[NotificationService] üîß Nettoyage forc√© des listeners');
    console.log('[NotificationService] üìä Listeners avant:', this.notificationListeners.length);
    this.notificationListeners = [];
    this.processedNotifications.clear();
    console.log('[NotificationService] ‚úÖ Listeners nettoy√©s');
  }
}

export default new NotificationService();